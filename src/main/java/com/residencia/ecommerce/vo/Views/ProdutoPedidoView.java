package com.residencia.ecommerce.vo.Views;

public class ProdutoPedidoView {
	
	private String nomeProduto;
	private Integer quantidadeProdutoNoPedido;
	
	public String getNomeProduto() {
		return nomeProduto;
	}
	public void setNomeProduto(String nomeProduto) {
		this.nomeProduto = nomeProduto;
	}
	public Integer getQuantidadeProdutoNoPedido() {
		return quantidadeProdutoNoPedido;
	}
	public void setQuantidadeProdutoNoPedido(Integer quantidadeProdutoNoPedido) {
		this.quantidadeProdutoNoPedido = quantidadeProdutoNoPedido;
	}
	
	

}
